/**
 * Validates if an email is from a disposable/temporary email service
 * @param {string} email - The email address to validate
 * @returns {boolean} - True if email is from a disposable domain, false otherwise
 */
export function isDisposableEmail(email) {
  if (!email || typeof email !== 'string') {
    return false;
  }

  const emailLower = email.toLowerCase().trim();
  const domain = emailLower.split('@')[1];

  if (!domain) {
    return false;
  }

  // Check against the list of disposable domains
  return DISPOSABLE_EMAIL_DOMAINS.includes(domain);
}

/**
 * Validates email format and checks if it's disposable
 * @param {string} email - The email address to validate
 * @returns {{valid: boolean, error?: string}} - Validation result
 */
export function validateEmail(email) {
  if (!email || typeof email !== 'string') {
    return { valid: false, error: 'Email is required' };
  }

  const emailTrimmed = email.trim();

  // Basic email format validation
  const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  if (!emailRegex.test(emailTrimmed)) {
    return { valid: false, error: 'Please enter a valid email address' };
  }

  // Check for disposable email
  if (isDisposableEmail(emailTrimmed)) {
    return {
      valid: false,
      error: 'Temporary or disposable email addresses are not allowed. Please use a permanent email address.',
    };
  }

  return { valid: true };
}

